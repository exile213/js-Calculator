function add(num1,num2){
    return num1+num2;
}
function subtract(num1,num2){
    return num1-num2;
}
function multiply(num1,num2){
    return num1*num2;
}
function divide(num1,num2){
    return num1/num2;
}

console.log(add(21,33));
console.log(subtract(45,23));
console.log(multiply(23,84));
console.log(divide(55,5));